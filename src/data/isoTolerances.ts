export function getRangeIndex(nominal: number): number {
  if (nominal > 1 && nominal <= 3) return 0;
  if (nominal > 3 && nominal <= 6) return 1;
  if (nominal > 6 && nominal <= 10) return 2;
  if (nominal > 10 && nominal <= 18) return 3;
  if (nominal > 18 && nominal <= 30) return 4;
  if (nominal > 30 && nominal <= 50) return 5;
  if (nominal > 50 && nominal <= 80) return 6;
  if (nominal > 80 && nominal <= 120) return 7;
  if (nominal > 120 && nominal <= 180) return 8;
  if (nominal > 180 && nominal <= 250) return 9;
  return -1;
}

